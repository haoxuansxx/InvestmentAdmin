package com.investment.entity;

/**
 * 图标实体类
 * @author Sun
 */
public class Icon {
    /**
     * 唯一标识ID
     */
    private Integer id;
    /**
     * 地址
     */
    private String url;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
